#include <iostream>
using namespace std;

int main()
{
    int counter = 7;

    // post increment
    counter++;
    cout << counter << endl;

    counter--;
    cout << counter << endl;

    // pre increment
    int counter2 = 2;
    cout << ++counter2 << endl;
    cout << --counter2 << endl;
    cout << counter2 << endl;

    // clear console
    //system("cls");

    // relational operators
    int a = 5, b = 5;
    cout << (a > b);


    system("pause>0");
}
